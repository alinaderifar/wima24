<script>
	onDocumentReady(() => {
		const hidePoweredByEl = document.querySelector("input[type=checkbox][name=hide_powered_by]");
		if (!hidePoweredByEl) return;

		// ????? ?????
		togglePoweredByFields(hidePoweredByEl);

		// ????? ?????
		hidePoweredByEl.addEventListener("change", e => togglePoweredByFields(e.target));
	});

	function togglePoweredByFields(checkboxEl) {
		// ??? ?? ???? ???? ???? ???? ?? ??? ??? ???? ????? ??
		const action = checkboxEl.checked ? "hide" : "show";
		setElementsVisibility(action, ".powered-by-field");
	}
</script>